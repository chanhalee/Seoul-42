char	*ft_strupcase(char	*str)
{
	int		temp;
	char	c;

	temp = 0;
	while (str[temp] != '\0')
	{
		c = str[temp];
		if ((c >= 97 && c <= 122))
		{
			str[temp] -= 32;
		}
		temp++;
	}
	return (str);
}
